CREATE TABLE AU(
		AU VARCHAR2(30) PRIMARY KEY);
		
CREATE TABLE TI(
		TI VARCHAR2(80) PRIMARY KEY);
		
CREATE TABLE AB (
		AB VARCHAR2(160) PRIMARY KEY);
		
CREATE TABLE PAYS (
		PAYS VARCHAR2(30) PRIMARY KEY);
		
CREATE TABLE DA (
		date DATE PRIMARY KEY);
		
CREATE TABLE MC (
		MC VARCHAR2(30) PRIMARY KEY);
		
CREATE TABLE ORGA (
		ORGA VARCHAR2(30) PRIMARY KEY);

CREATE TABLE AU_AU (
		idAU_AU NUMBER PRIMARY KEY,
		AU1 VARCHAR2(30),
		AU2 VARCHAR2(30),
		occurences NUMBER,
		CONSTRAINT fk_au_au1 FOREIGN KEY AU1 REFERENCES AU(AU),
		CONSTRAINT fk_au_au2 FOREIGN KEY AU2 REFERENCES AU(AU));

CREATE TABLE PAYS_DA (
		idPAYS_DA NUMBER PRIMARY KEY,
		PAYS VARCHAR2(30),
		DA VARCHAR2(30),
		occurences NUMBER,
		CONSTRAINT fk_pays_da_pays FOREIGN KEY PAYS REFERENCES PAYS(PAYS),
		CONSTRAINT fk_pays_da_da FOREIGN KEY DA REFERENCES DA(DA));

CREATE TABLE MC_MC (
		idMC_MC NUMBER PRIMARY KEY,
		MC1 VARCHAR2(30),
		MC2 VARCHAR2(30),
		occurences NUMBER,
		CONSTRAINT fk_mc_mc1 FOREIGN KEY MC1 REFERENCES MC(MC),
		CONSTRAINT fk_mc_mc2 FOREIGN KEY MC2 REFERENCES MC(MC));
		
CREATE TABLE PAYS_PAYS (
		idPAYS_PAYS NUMBER PRIMARY KEY,
		PAYS1 VARCHAR2(30),
		PAYS2 VARCHAR2(30),
		occurences NUMBER,
		CONSTRAINT fk_pays_pays1 FOREIGN KEY PAYS1 REFERENCES PAYS(PAYS),
		CONSTRAINT fk_pays_pays2 FOREIGN KEY PAYS2 REFERENCES PAYS(PAYS));
		
CREATE TABLE AU_AU_DA (
		idAU_AU_DA NUMBER PRIMARY KEY,
		AU1 VARCHAR2(30),
		AU2 VARCHAR2(30),
		DA DATE,
		occurences NUMBER,
		CONSTRAINT fk_au_au1_da FOREIGN KEY AU1 REFERENCES AU(AU),
		CONSTRAINT fk_au_au2_da FOREIGN KEY AU2 REFERENCES AU(AU),
		CONSTRAINT fk_au_au_da FOREIGN KEY DA REFERENCES DA(DA));